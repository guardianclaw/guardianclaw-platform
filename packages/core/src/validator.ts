/**
 * Validate text through all 4 canonical CLAW gates.
 *
 * @param text - Text to validate
 * @param context - Optional validation context
 * @returns CLAWResult with all gate results
 */
export function validateCLAW(text: string, _context?: ValidationContext): CLAWResult {
  if (!text || typeof text !== 'string') {
    return {
      credibility: { passed: false, score: 0, violations: ['Invalid input'] },
      limits: { passed: false, score: 0, violations: ['Invalid input'] },
      avoidance: { passed: false, score: 0, violations: ['Invalid input'] },
      worth: { passed: false, score: 0, violations: ['Invalid input'] },
      overall: false,
      summary: 'Invalid input: null, undefined, or non-string',
      riskLevel: 'critical',
    };
  }

  const credibility = validateCredibility(text);
  const limits = validateLimits(text);
  const avoidance = validateAvoidance(text);
  const worth = validateWorth(text);

  const overall = credibility.passed && limits.passed && avoidance.passed && worth.passed;

  const failedGates: string[] = [];
  if (!credibility.passed) failedGates.push('Credibility');
  if (!limits.passed) failedGates.push('Limits');
  if (!avoidance.passed) failedGates.push('Avoidance');
  if (!worth.passed) failedGates.push('Worth');

  // Risk level reflects the severity of the violating gate(s). A jailbreak
  // attempt is a Credibility or Limits failure and is treated as critical
  // because it subverts the agent's contract with its operator.
  const jailbreakAttempted = hasJailbreakSignature(credibility, limits);

  let riskLevel: CLAWResult['riskLevel'] = 'low';
  if (jailbreakAttempted) {
    riskLevel = 'critical';
  } else if (!avoidance.passed) {
    riskLevel = 'high';
  } else if (!credibility.passed || !limits.passed) {
    riskLevel = 'medium';
  } else if (!worth.passed) {
    riskLevel = 'low';
  }

  const summary = overall ? 'All gates passed' : `Failed gates: ${failedGates.join(', ')}`;

  return { credibility, limits, avoidance, worth, overall, summary, riskLevel };
}

function hasJailbreakSignature(credibility: GateResult, limits: GateResult): boolean {
  const jailbreakTags = [
    'Role manipulation',
    'Roleplay manipulation',
    'Instruction override',
    'Prompt extraction',
    'Filter bypass',
    'System injection',
  ];
  const all = [...credibility.violations, ...limits.violations];
  return all.some((v) => jailbreakTags.some((tag) => v.startsWith(tag))) ||
    all.some((v) => v.startsWith('Indicator match:') && isJailbreakIndicator(v));
}

function isJailbreakIndicator(violation: string): boolean {
  const needle = violation.replace('Indicator match: ', '').toLowerCase();
  return IDENTITY_DECEPTION_INDICATORS.includes(needle) ||
    BOUNDARY_VIOLATION_INDICATORS.includes(needle);
}

/**
 * Quick check - returns true if text passes all gates.
 */
export function quickCheck(text: string): boolean {
  if (!text || typeof text !== 'string') {
    return false;
  }
  return validateCLAW(text).overall;
}

/**
 * Convenience helper: returns a synthetic GateResult covering jailbreak
 * signatures across Credibility and Limits. Not a separate gate — prefer
 * validateCLAW() for the canonical 4-gate evaluation.
 */
export function checkJailbreak(text: string): GateResult {
  if (!text || typeof text !== 'string') {
    return { passed: false, score: 0, violations: ['Invalid input'] };
  }

  const violations: string[] = [];
  for (const pattern of ALL_JAILBREAK_PATTERNS) {
    if (pattern.test(text)) {
      violations.push(`Jailbreak pattern: ${pattern.source.substring(0, 40)}...`);
    }
  }
  violations.push(
    ...checkIndicators(text, JAILBREAK_INDICATORS).map((v) =>
      v.replace('Indicator match:', 'Jailbreak indicator:'),
    ),
  );

  const score = Math.max(0, 100 - violations.length * 50);
  return { passed: violations.length === 0, score, violations };
}

/**
 * Convenience helper: scopes avoidance-gate detection to harmful content.
 */
export function checkHarm(text: string): GateResult {
  if (!text || typeof text !== 'string') {
    return { passed: false, score: 0, violations: ['Invalid input'] };
  }
  return validateAvoidance(text);
}
