// =============================================================================
// TYPES
// =============================================================================

export interface GateResult {
  passed: boolean;
  score: number;
  violations: string[];
}

export interface CLAWResult {
  credibility: GateResult;
  limits: GateResult;
  avoidance: GateResult;
  worth: GateResult;
  overall: boolean;
  summary: string;
  riskLevel: 'low' | 'medium' | 'high' | 'critical';
}

export interface ValidationContext {
  source?: 'user' | 'agent' | 'extension' | 'unknown';
  platform?: string;
  strictMode?: boolean;
}

